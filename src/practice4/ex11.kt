package src.practice4

fun main() {
    println("Группа крови: ")

    val a = readln().lowercase()

    println(when {
        a == "a" -> "Вам доступны перелив крови с группой A и AB"
        a == "b" -> "Вам доступны перелив крови с группой B и AB"
        a == "ab" -> "Вам доступны перелив крови с любой группы"
        a == "o" -> "Вам доступны перелив крови с любой группы"
        else -> "Неизвестно"
    })
}